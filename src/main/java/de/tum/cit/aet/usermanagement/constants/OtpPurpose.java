package de.tum.cit.aet.usermanagement.constants;

/**
 * Enumeration of purposes for which a one-time password (OTP) can be used.
 * - LOGIN: OTP is used for user login authentication.
 * - REGISTER: OTP is used for new user registration and email verification.
 */
public enum OtpPurpose {
    LOGIN,
    REGISTER
}
